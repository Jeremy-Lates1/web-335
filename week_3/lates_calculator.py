#add function
def add(value1, value2):
  return value1+value2

#subtract function
def subtract(value1, value2):
  return value1-value2

#divide function
def divide(value1,value2):
  return value1/value2

#multiple function 
def multiple(value1,value2):
  return value1*value2

#set the variables with operational function
x = add(4,4)

y= subtract(10,6)

z= divide(8,2)

a=multiple(10,2)

#Show the results
print ("4 + 4 is ", x)
print ("10 - 6 is ", y)
print ("8 / 2 is ", z)
print ("10 * 2 is ", a)


